# Q3) Using max() and min() functions display the maximum and minimum of 5 random numbers.

# 5 random numbers
random_numbers = [69, 88, 47, 51, 32]

# Find the maximum and minimum numbers using max() and min() functions
max = max(random_numbers)
min = min(random_numbers)

# Print the results
print("Maximum number is:", max) 
print("Minimum number is:", min)


'''
OutPut:- 
Maximum number is: 88
Minimum number is: 32
'''
